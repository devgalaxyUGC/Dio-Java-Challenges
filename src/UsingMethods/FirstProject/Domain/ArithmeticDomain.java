package UsingMethods.FirstProject.Domain;

public class ArithmeticDomain {
    public void sumNumbers(double a, double b){
        System.out.println(a + b);
    }

    public void subNumbers(double a, double b){
        System.out.println(a - b);
    }

    public void multNumbers(double a, double b){
        System.out.println(a * b);
    }

    public void divNumbers(double a, double b){
        System.out.println(a / b);
    }

    public void restdiv(double a, double b){
        System.out.println(a % b);
    }
}

